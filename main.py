import pandas as pd
import numpy as np

# 1. Создаем DataFrame с оценками 10 учеников по 5 предметам
data = {
    'Имя': ['Анна', 'Борис', 'Виктор', 'Галя', 'Дима', 'Елена', 'Женя', 'Зоя', 'Игорь', 'Катя'],
    'Математика': [85, 90, 78, 92, 70, 88, 76, 95, 89, 84],
    'Русский язык': [78, 82, 85, 80, 76, 88, 90, 92, 79, 81],
    'Физика': [90, 85, 80, 88, 82, 75, 85, 87, 89, 83],
    'Химия': [84, 79, 85, 87, 80, 90, 83, 82, 88, 86],
    'Информатика': [95, 92, 88, 90, 85, 93, 87, 96, 94, 91]
}

df = pd.DataFrame(data)

# 2. Выводим первые несколько строк
print("Первые строки DataFrame:")
print(df.head(), '\n')

# 3. Средняя оценка по каждому предмету
mean_scores = df.drop(columns='Имя').mean()
print("Средняя оценка по каждому предмету:")
print(mean_scores, '\n')

# 4. Медианная оценка по каждому предмету
median_scores = df.drop(columns='Имя').median()
print("Медианная оценка по каждому предмету:")
print(median_scores, '\n')

# 5. Q1 и Q3 для оценок по математике
Q1_math = df['Математика'].quantile(0.25)
Q3_math = df['Математика'].quantile(0.75)
IQR_math = Q3_math - Q1_math
print(f"Q1 по математике: {Q1_math}")
print(f"Q3 по математике: {Q3_math}")
print(f"IQR по математике: {IQR_math}\n")

# 6. Стандартное отклонение по каждому предмету
std_scores = df.drop(columns='Имя').std()
print("Стандартное отклонение по каждому предмету:")
print(std_scores)
